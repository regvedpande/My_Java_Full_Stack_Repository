// The typeof operator helps us check the data type of each variable.
//For Numbers
let age = 23;
let price = 99.99;

console.log(typeof age);
console.log(typeof price);

//String
let name = "Regved Pande";
let anothername = 'Thomas Shelby';

console.log(typeof name);
console.log(typeof anothername);

//Boolean
let sunexists = true;
let dragonexists = false;

console.log(typeof sunexists);
console.log(typeof dragonexists);

//Null
let boxisempty = null;

console.log(typeof boxisempty);

//Undefined
let myhobby;

console.log(typeof myhobby);

//BigInt
let mobile = 9049874883n;

console.log(typeof mobile);