let a = 9;
let b = 8;
console.log(a == b);
console.log(a != b);

// b = "9"; can say false if compare a==b as their types are different 
// a === b (Strict Equality): Checks if 'a' and 'b' have both the same value and the same data type.
console.log(a === b);

// a !== b (Strict Inequality): Checks if 'a' and 'b' have different values or different data types.
console.log(a !== b);
console.log(a > b);
console.log(a < b);